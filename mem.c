#include "mem.h"

#include <errno.h>
#include <inttypes.h>
#include <stdint.h>
#include <stdio.h>
#include <stdlib.h>
#include <string.h>

void *xcalloc(size_t s)
{
	void *p;
	
	p = calloc(1, s);
	if (!p) {
		fprintf(stderr, "out of memory allocating %zd bytes.\n", s);
		abort();
	}
	return p;
}

char *xstrdup(const char *str)
{
	char *s = strdup(str);
	if (!s) {
		fprintf(stderr, "failed to duplicate string of size "
			"%zd.\n", strlen(str));
		exit(1);
	}
	return s;
}
